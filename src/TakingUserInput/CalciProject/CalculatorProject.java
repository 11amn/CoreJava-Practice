package TakingUserInput.CalciProject;

public class CalculatorProject {
    int add (int a, int b) {
        return a + b;
    }

    int sub (int a, int b) {
        return a - b;
    }

    int multiply (int a, int b) {
        return a * b;
    }

    double div (int a, int b) {
        return (double) a / b;
    }
}
