package Operator;

public class BasicCalci {
    public static void main(String[] args) {
        var a = 12;
        var b = 6;
        var sum = a + b;
        var difference = a - b;
        var product = a * b;
        var quotient = a / b;
        System.out.println("Sum: " + sum);
        System.out.println("Difference: " + difference);
        System.out.println("Product: " + product);
        System.out.println("Quotient: " + quotient);
    }
}
