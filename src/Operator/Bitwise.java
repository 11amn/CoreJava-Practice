package Operator;

public class Bitwise {
    public static void main(String[] args) {

        // Right Shift >>
        var a = 32;
        var result = a >> 2;
        System.out.println(result);

        // Left Shift <<
        var b = 10;
        var res = b << 3;
        System.out.println(res);

    }
}
